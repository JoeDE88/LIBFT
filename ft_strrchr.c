#include "libft.h"
#include <stdio.h>

char	*ft_strrchr(const char *s, int c)
{
	int				i;

	if ((char)c == '\0')
		return ((char *)&s[ft_strlen(s)]);
	i = ft_strlen(s);
	if (i <= 0)
		return (NULL);
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
		i--;
	}
	return (NULL);
}
